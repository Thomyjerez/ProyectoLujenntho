export class Producto {
  constructor(codigo, nombre, descripcion, imagen, imagenSecundaria, categoria, precio, stock) {
    this.codigo = codigo;
    this.nombre = nombre;
    this.descripcion = descripcion;
    this.imagen = imagen;
    this.imagenSecundaria = imagenSecundaria;
    this.categoria = categoria;
    this.precio = precio;
    this.stock = stock;
  }
}


